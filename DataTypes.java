public class DataTypes {
    public static void main(String[] args){
        byte b = 2;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean var = true;
        System.out.println(var);
        int num = 5;
        System.out.println(num);
        float price = 2.25f;
        System.out.println(price);
        //Long
        //Double
        short s = 20;
        System.out.println(s);

    }
}
